def predict_ensemble(texts, weights=(0.6,0.2,0.2)):
    wt, wlr, wrnn = weights
    N = len(texts)
    # get probabilities if possible
    probs_tr = probs_lr = probs_rnn = None
    try:
        _, probs_tr = predict_transformer(texts)
    except Exception as e:
        print("Transformer probs unavailable:", e)
    try:
        _, probs_lr = predict_lr(texts)
    except Exception as e:
        print("LR probs unavailable:", e)
    try:
        _, probs_rnn = predict_rnn(texts)
    except Exception as e:
        print("RNN probs unavailable:", e)

    final_probs = np.zeros(N, dtype=float)
    weight_sum = 0.0
    if probs_tr is not None:
        final_probs += wt * probs_tr; weight_sum += wt
    if probs_lr is not None:
        final_probs += wlr * probs_lr; weight_sum += wlr
    if probs_rnn is not None:
        final_probs += wrnn * probs_rnn; weight_sum += wrnn
    if weight_sum == 0:
        raise RuntimeError("No model probabilities available for ensemble.")
    final_probs = final_probs / weight_sum
    final_preds = (final_probs >= 0.5).astype(int)
    return final_preds, final_probs

