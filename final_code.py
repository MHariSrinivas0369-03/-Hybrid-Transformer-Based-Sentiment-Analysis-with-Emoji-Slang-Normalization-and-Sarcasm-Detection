def predict_transformer(texts, batch_size=64):
    if distil_model is None or distil_tokenizer is None:
        raise RuntimeError("DistilBERT missing")
    all_preds=[]; all_probs=[]
    with torch.no_grad():
        for i in range(0, len(texts), batch_size):
            batch = texts[i:i+batch_size]
            enc = distil_tokenizer(batch, padding=True, truncation=True, max_length=128, return_tensors='pt')
            input_ids = enc['input_ids'].to(DEVICE)
            attn = enc['attention_mask'].to(DEVICE)
            out = distil_model(input_ids=input_ids, attention_mask=attn)
            logits = out.logits
            probs = torch.nn.functional.softmax(logits, dim=-1)[:,1].cpu().numpy()
            preds = np.argmax(logits.cpu().numpy(), axis=1)
            all_preds.extend(preds.tolist())
            all_probs.extend(probs.tolist())
    return np.array(all_preds), np.array(all_probs)

