# We need to build a custom vocabulary for the RNN's Embedding layer
class RnnVocab:
    def __init__(self, max_features=10000):
        self.word2idx = {'<PAD>': 0, '<UNK>': 1}
        self.idx2word = {0: '<PAD>', 1: '<UNK>'}
        self.max_features = max_features
        self.vectorizer = CountVectorizer(max_features=max_features, stop_words='english')

    def fit(self, texts):
        self.vectorizer.fit(texts)
        for i, word in enumerate(self.vectorizer.get_feature_names_out(), 2):
            self.word2idx[word] = i
            self.idx2word[i] = word

    def transform(self, text):
        words = text.split()
        return [self.word2idx.get(word, 1) for word in words] # 1 is <UNK>

    def __len__(self):
        return len(self.word2idx)

# Create and fit the vocabulary on the same training data
