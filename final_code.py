# 2) Minimal RnnVocab definition (needed so joblib.load can reconstruct the object)
#    This must match attribute names used when you created/saved rnn_vocab.
class RnnVocab:
    def __init__(self, max_features=10000):
        self.word2idx = {'<PAD>': 0, '<UNK>': 1}
        self.idx2word = {0: '<PAD>', 1: '<UNK>'}
        self.max_features = max_features
    def transform(self, text):
        # simple split-based transform; joblib will restore saved mapping inside object
        words = str(text).split()
        return [self.word2idx.get(w, 1) for w in words]
    def __len__(self):
        return len(self.word2idx)

# 3) Optional: define LSTM model class (must match saved checkpoint)
class LSTM_Model(nn.Module):
    def __init__(self, vocab_size, embedding_dim, hidden_dim, output_dim, n_layers, dropout):
        super().__init__()
        self.embedding = nn.Embedding(vocab_size, embedding_dim, padding_idx=0)
        self.lstm = nn.LSTM(embedding_dim, hidden_dim, num_layers=n_layers,
                            bidirectional=True, batch_first=True, dropout=dropout)
        self.fc = nn.Linear(hidden_dim * 2, output_dim)
        self.dropout = nn.Dropout(dropout)
    def forward(self, text):
        embedded = self.dropout(self.embedding(text))
        outputs, (hidden, cell) = self.lstm(embedded)
        hidden_cat = self.dropout(torch.cat((hidden[-2,:,:], hidden[-1,:,:]), dim=1))
        return self.fc(hidden_cat)

