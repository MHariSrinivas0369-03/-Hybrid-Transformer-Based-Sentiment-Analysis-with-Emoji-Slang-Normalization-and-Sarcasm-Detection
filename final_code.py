    rnn_val_loader = DataLoader(rnn_val_dataset, batch_size=32, collate_fn=collate_fn_pad)

    # Define the LSTM Model
    class LSTM_Model(nn.Module):
        def __init__(self, vocab_size, embedding_dim, hidden_dim, output_dim, n_layers, dropout):
            super().__init__()
            self.embedding = nn.Embedding(vocab_size, embedding_dim, padding_idx=0)
            self.lstm = nn.LSTM(embedding_dim, hidden_dim, num_layers=n_layers,
                                bidirectional=True, batch_first=True, dropout=dropout)
            self.fc = nn.Linear(hidden_dim * 2, output_dim) # *2 for bidirectional
            self.dropout = nn.Dropout(dropout)

        def forward(self, text):
            # text shape: [batch_size, seq_len]
            embedded = self.dropout(self.embedding(text))
            # embedded shape: [batch_size, seq_len, embedding_dim]

            outputs, (hidden, cell) = self.lstm(embedded)
            # outputs shape: [batch_size, seq_len, hidden_dim * 2]

            # Concat the final forward and backward hidden states
            hidden = self.dropout(torch.cat((hidden[-2,:,:], hidden[-1,:,:]), dim=1))
            # hidden shape: [batch_size, hidden_dim * 2]

            return self.fc(hidden)

