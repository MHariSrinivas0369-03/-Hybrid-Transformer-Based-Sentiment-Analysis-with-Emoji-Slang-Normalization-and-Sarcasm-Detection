WEIGHTS = {
    'transformer': 0.6,
    'lr': 0.2,
    'rnn': 0.2
}

def predict_hybrid_sentiment(text):
    """
    Analyzes a single text string using the full hybrid system.
    """
    results = {}

    # 1. Preprocess text for LR/RNN
    processed_text = preprocess_text(text)

    with torch.no_grad():
        # --- Model 1: Sarcasm Prediction (using raw text) ---
        inputs = sarcasm_tokenizer(text, return_tensors='pt', truncation=True, max_length=128).to(device)
        outputs = sarcasm_model(**inputs)
        probs = torch.nn.functional.softmax(outputs.logits, dim=-1)[0]
        sarc_prob = probs[1].item() # Index 1 is 'sarcasm'
        is_sarcastic = sarc_prob > 0.9
        results['sarcasm'] = {
            'label': "Sarcastic" if is_sarcastic else "Not Sarcastic",
            'confidence': sarc_prob if is_sarcastic else 1.0 - sarc_prob
        }

        # --- Model 2: DistilBERT Prediction (using raw text) ---
        inputs = distilbert_tokenizer(text, return_tensors='pt', truncation=True, max_length=128).to(device)
        outputs = distilbert_model(**inputs)
        probs = torch.nn.functional.softmax(outputs.logits, dim=-1)[0]
        results['transformer'] = {
            'label': "Positive" if probs[1] > probs[0] else "Negative",
            'prob_positive': probs[1].item()
        }

        # --- Model 3: Logistic Regression Prediction (using processed text) ---
        tfidf_features = tfidf_vectorizer.transform([processed_text])
        probs = lr_model.predict_proba(tfidf_features)[0]
        results['lr'] = {
            'label': "Positive" if probs[1] > probs[0] else "Negative",
            'prob_positive': probs[1]
        }

        # --- Model 4: RNN/LSTM Prediction (using processed text) ---
        token_ids = rnn_vocab.transform(processed_text)
        if not token_ids: # Handle empty string after processing
            token_ids = [0] # Use <PAD> token
        tensor = torch.tensor([token_ids], dtype=torch.long).to(device)

        prediction = rnn_model(tensor)
        probs = torch.nn.functional.softmax(prediction, dim=-1)[0]
        results['rnn'] = {
            'label': "Positive" if probs[1] > probs[0] else "Negative",
            'prob_positive': probs[1].item()
        }

    # --- Final Combination: Weighted Voting ---
    final_prob_positive = (
        WEIGHTS['transformer'] * results['transformer']['prob_positive'] +
        WEIGHTS['lr'] * results['lr']['prob_positive'] +
        WEIGHTS['rnn'] * results['rnn']['prob_positive']
    )

    results['weighted_average'] = final_prob_positive

    # --- Sarcasm Adjustment ---
    sarcasm_adjusted_prob = final_prob_positive
    adjustment_applied = False

    # If the model is sarcastic AND the initial sentiment is positive,
    # it's likely a negative comment. We invert the sentiment.
    if is_sarcastic and final_prob_positive > 0.5:
        sarcasm_adjusted_prob = 1.0 - final_prob_positive
        adjustment_applied = True

    final_label = "Positive" if sarcasm_adjusted_prob > 0.5 else "Negative"
    final_confidence = max(sarcasm_adjusted_prob, 1.0 - sarcasm_adjusted_prob)

    results['final_prediction'] = {
        'label': final_label,
        'confidence': final_confidence,
        'sarcasm_adjustment_applied': adjustment_applied
    }

    return results

print("Hybrid prediction function defined and ready.")

