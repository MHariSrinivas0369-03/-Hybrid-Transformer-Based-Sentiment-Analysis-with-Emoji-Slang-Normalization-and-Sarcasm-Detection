def preprocess_text(text):
    """
    Cleans and preprocesses text for ML/RNN models.
    - Converts emojis to text
    - Replaces common slang
    - Removes URLs, user mentions, and hashtags
    - Removes special characters and numbers
    - Converts to lowercase
    """
    if not isinstance(text, str):
        text = str(text)

    # 1. Convert emojis to text descriptions
    text = emoji.demojize(text, delimiters=(" ", " ")) # "I ❤️" -> "I :red_heart: "
    text = text.replace("_", " ") # "red_heart" -> "red heart"

    # 2. Remove URLs
    text = re.sub(r'http\S+|www\S+|https\S+', '', text, flags=re.MULTILINE)

    # 3. Remove user @mentions
    text = re.sub(r'@\w+', '', text)

    # 4. Remove #hashtags (keep the text)
    text = re.sub(r'#(\w+)', r'\1', text)

    # 5. Convert to lowercase
    text = text.lower()

    # 6. Replace slang
    # This is a simple implementation; a more robust one would use word boundaries
    words = text.split()
    reformed = [SLANG_DICT[word] if word in SLANG_DICT else word for word in words]
    text = " ".join(reformed)

    # 7. Remove non-alphabetic characters (but keep spaces)
    text = re.sub(r'[^a-zA-Z\s]', '', text)

    # 8. Remove extra whitespace
    text = re.sub(r'\s+', ' ', text).strip()

    return text

